package si.pint.activiti.standalone.identity;

import org.activiti.engine.impl.cfg.IdentitySession;
import org.activiti.engine.impl.interceptor.Session;
import org.activiti.engine.impl.interceptor.SessionFactory;

/**
 * Custom identitySessionFactory that returns an a custom IdentitySession.
 * 
 * @author Nils Preusker (nils.preusker@camunda.com)
 */
public class CustomIdentitySessionFactory implements SessionFactory {

  @Override
public Session openSession() {
    return new CustomIdentitySession();
  }

  @Override
public Class< ? > getSessionType() {
    return IdentitySession.class;
  }

}
